#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*line;

	i = 0;
	line = s;
	while (n > 0)
	{
		line[i] = '\0';
		i++;
		n--;
	}
	s = line;
}
